public class Usuario  {
    private int id;
    private String nombre;
    private String rol;
    
    public Usuario() {
        this.id = id;
        this.nombre = nombre;
        this.rol = rol;
    }

      public int getid() {
        return id;
    }
      public String getnombre() {
        return nombre;
    }
      public String getrol() {
        return rol;
    }
      
      public void setid(int id){
        this.id = id;
    }
       public void setnombre(String nombre){
        this.nombre = nombre;
    }
        public void setrol (String rol){
        this.rol = rol;
   } 
}      
